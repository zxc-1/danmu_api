// Global state for WASM
let yf = null;
let Mf = null;
let jf = null;
let memoryRef = null;
let Pf = 0;

// Ef - object reference table
const Ef = new Array(132).fill(undefined);
Ef.push(void 0, null, true, false);
let Af = Ef.length;

function Cf(l) {
    const n = Ef[l];
    if (l >= 132) {
        Ef[l] = Af;
        Af = l;
    }
    return n;
}

function If() {
    if (jf === null || jf.byteLength === 0) {
        jf = new Uint8Array(memoryRef.buffer);
    }
    return jf;
}

function Df() {
    if (Mf === null || Mf.byteLength === 0) {
        Mf = new Int32Array(memoryRef.buffer);
    }
    return Mf;
}

function Tf(l, n) {
    l = l >>> 0;
    return utf8BytesToString(If().subarray(l, l + n));
}

function Nf(l, n, u) {
    if (u === undefined) {
        const t = stringToUtf8Bytes(l);
        const e = n(t.length, 1) >>> 0;
        If().subarray(e, e + t.length).set(t);
        Pf = t.length;
        return e;
    }
    const t = stringToUtf8Bytes(l);
    const e = n(t.length, 1) >>> 0;
    If().subarray(e, e + t.length).set(t);
    Pf = t.length;
    return e;
}

function Uf(l) {
    return l == null;
}

function base64ToUint8Array(base64) {
    const binaryString = atob(base64.replace(/\s+/g, ''));
    const bytes = new Uint8Array(binaryString.length);
    for (let i = 0; i < binaryString.length; i++) {
        bytes[i] = binaryString.charCodeAt(i);
    }
    return bytes;
}

async function initWasm() {
    if (yf !== null) {
        return yf;
    }

    //  base64  WASM
    const wasmBuffer = base64ToUint8Array(WASM_BASE64);

    const importObject = {
        wbg: {
            __wbindgen_object_drop_ref: (l) => Cf(l),
            __wbindgen_string_new: (ptr, len) => {
                const str = Tf(ptr, len);
                if (Af === Ef.length) {
                    Ef.push(Ef.length + 1);
                }
                const idx = Af;
                Af = Ef[idx];
                Ef[idx] = str;
                return idx;
            },
            __wbindgen_debug_string: () => {}
        }
    };

    const result = await WebAssembly.instantiate(wasmBuffer, importObject);
    yf = result.instance.exports;
    memoryRef = yf.memory;
    Mf = null;
    jf = null;

    return yf;
}

function Hf(l, n) {
    let u, t;
    try {
        const e = yf.__wbindgen_add_to_stack_pointer(-16);
        const r = Nf(l, yf.__wbindgen_malloc, yf.__wbindgen_realloc);
        const o = Pf;
        const i = Uf(n) ? 0 : Nf(n, yf.__wbindgen_malloc, yf.__wbindgen_realloc);
        const a = Pf;

        yf.gateway_decrypt(e, r, o, i, a);

        const s = Df()[e / 4 + 0];
        const c = Df()[e / 4 + 1];
        const f = Df()[e / 4 + 2];
        const p = Df()[e / 4 + 3];
        let d = s;
        let h = c;

        if (p) {
            d = 0;
            h = 0;
            throw Cf(f);
        }

        u = d;
        t = h;
        return Tf(d, h);
    } finally {
        yf.__wbindgen_add_to_stack_pointer(16);
        if (u !== undefined && t !== undefined) {
            yf.__wbindgen_free(u, t, 1);
        }
    }
}

function Ff() {
    const idx = yf.get_key();
    return Ef[idx];
}

export async function decrypt(encryptedData, key) {
    encryptedData = encryptedData.replace(/\s+/g, '');
    await initWasm();
    return Hf(encryptedData, key);
}

